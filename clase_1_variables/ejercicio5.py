"""
Ejercicio 5:
Escriba un programa que solicite un valor entero por pantalla y presente en la terminal del editor el valor incrementado en un 10%.

"""

float_numero = float(input("Ingrese un número: "))
incremento = float_numero * 0.10

print (f"El incremento del 10% de {float_numero} es: {incremento}")
print (f"El número incrementado en un 10% es: {float_numero + incremento}")